def multilingual_qa_bot(user_input):
    print("Raw User Input:", user_input)                        # ğŸ–¨ï¸ Log raw input

    try:
        overall_lang = detect(user_input)                      # ğŸŒ Detect input language
        if overall_lang not in ['ta', 'hi', 'bn', 'en']:       # âŒ Limit supported languages
            overall_lang = 'en'
    except:
        overall_lang = "en"                                     # ğŸ›‘ Default to English on detection fail

    user_input_fixed = preserve_named_entities(user_input, overall_lang)  # ğŸ”„ Preserve named entities
    question_list = split_questions(user_input_fixed)           # âœ‚ï¸ Split multiple questions

    answers = []
    for q in question_list:
        is_valid, validation_msg = is_valid_input(q)           # âœ… Validate input question
        if not is_valid:
            answers.append(f"ğŸ‘‰ {validation_msg}")             # ğŸ›‘ Append validation failure message
            log_interaction(q, guvi_related=False, fallback_used="invalid_input")  # ğŸ“¥ Log interaction
            continue

        try:
            q_lang = detect(q)                                  # ğŸŒ Detect language per question
            if q_lang not in ['ta', 'hi', 'bn', 'en']:
                if re.search(r"[a-zA-Z]", q):
                    q_lang = 'en'
                else:
                    q_lang = overall_lang
        except:
            q_lang = overall_lang

        q_fixed = preserve_named_entities(q, q_lang)           # ğŸ”„ Preserve named entities again
        q_en = GoogleTranslator(source=q_lang, target="en").translate(q_fixed) if q_lang != "en" else q_fixed  # ğŸŒ Translate question to English

        guvi_flag = is_guvi_related(q_en)                      # ğŸ” Check if question is GUVI-related
        if not guvi_flag:
            answers.append("ğŸ‘‰ Sorry, I can only answer questions related to GUVI. Please contact ğŸ“§ support@guvi.in for other queries.")
            log_interaction(q, guvi_related=False, fallback_used="non_guvi_query")
            continue

        en_answer = find_best_answer(q_en)                      # ğŸ” Semantic FAQ search
        fallback_used = "semantic_search"

        if en_answer.lower().startswith("âš ï¸ couldn't find an exact match") or "couldn't understand" in en_answer.lower():
            en_answer = rag_fallback(q_en)                      # ğŸ” RAG fallback
            fallback_used = "RAG_fallback"
            if not en_answer or "no relevant answer" in en_answer.lower():
                en_answer = gpt_fallback(q_en)                  # ğŸ¤– GPT fallback
                fallback_used = "GPT_fallback"

        try:
            final_answer = GoogleTranslator(source='en', target=q_lang).translate(en_answer) if q_lang != "en" else en_answer  # ğŸŒ Translate back to user language
        except:
            final_answer = en_answer                            # ğŸ›‘ On failure, use English

        answers.append(f"ğŸ‘‰ {final_answer}")                    # ğŸ’¬ Append final answer

        log_interaction(q, guvi_related=True, fallback_used=fallback_used)  # ğŸ“¥ Log successful interaction

    return "\n\n".join(answers)                                 # ğŸ”„ Return all answers combined

