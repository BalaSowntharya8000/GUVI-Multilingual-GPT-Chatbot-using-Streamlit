def is_valid_input(text):
    if not text or len(text.strip()) < 3:                   # âŒ Reject empty or too short input
        return False, "â— Please enter a valid question, not just numbers or symbols."

    if re.fullmatch(r"[0-9\s\W_]+", text):                  # âŒ Reject inputs with only numbers/symbols
        return False, "â— Please enter a valid question, not just numbers or symbols."

    if len(text.strip()) <= 5:                              # âŒ Reject too short meaningful input
        return False, "Sorry, I couldn't understand your question. Please rephrase or contact ğŸ“§ support@guvi.in for help."

    if re.fullmatch(r"[a-zA-Z]+", text.strip()) and len(text.strip()) < 10:  # âŒ Reject short alphabetic-only strings
        return False, "Sorry, I couldn't understand your question. Please rephrase or contact ğŸ“§ support@guvi.in for help."

    return True, ""

